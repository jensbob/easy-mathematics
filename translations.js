let currentLang = 'he';

function updateLanguage(lang) {
    currentLang = lang;
    document.documentElement.lang = lang;
    document.documentElement.dir = lang === 'he' ? 'rtl' : 'ltr';
    
    // Update all elements with data-lang attribute
    document.querySelectorAll('[data-lang]').forEach(el => {
        const key = el.getAttribute('data-lang');
        if (translations[lang][key]) {
            el.textContent = translations[lang][key];
        }
    });
    
    // Update language toggle button
    const langBtn = document.getElementById('lang-toggle');
    if (langBtn) {
        langBtn.innerHTML = lang === 'he' ? `${UI_SVGS.flagEn} English` : `${UI_SVGS.flagHe} עברית`;
    }
    
    // Save preference
    localStorage.setItem('lang', lang);

    // Update grade-dependent text if a grade is selected
    if (typeof currentGrade !== 'undefined' && currentGrade !== null) {
        if (typeof updateCategoryCards === 'function') updateCategoryCards();
        if (typeof updateGradeIndicator === 'function') updateGradeIndicator();
    }
}

function t(key) {
    return translations[currentLang][key] || key;
}
